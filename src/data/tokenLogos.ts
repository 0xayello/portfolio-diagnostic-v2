/**
 * Get the logo URL for a token symbol
 */
export function getTokenLogo(symbol: string): string {
  const upperSymbol = symbol.toUpperCase();
  return TOKEN_LOGOS[upperSymbol] || 
    `https://ui-avatars.com/api/?name=${upperSymbol}&background=3ecf8e&color=1a1b4b&size=64&bold=true&format=png`;
}

/**
 * Get the CoinGecko ID for a token symbol
 */
export function getCoinGeckoId(symbol: string): string | undefined {
  return SYMBOL_TO_COINGECKO_ID[symbol.toUpperCase()];
}
